#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int l;
	int i;

	l = (int)ft_strlen(s);
	i = l;
	while (i >= 0)
	{
		if (s[i] == (unsigned char)c)
			return ((char *)s + i);
		i--;
	}
	return (NULL);
}
